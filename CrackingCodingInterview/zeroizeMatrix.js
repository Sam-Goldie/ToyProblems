const zeroizeMatrix = (matrix) => {
  for (let i = 0; i < matrix.length; i++) {
    for (let j = 0; j < matrix[i].length; j++) {
      if (matrix[i][j] === 0) {
        nullify(matrix, i, j);
      } 
    }
  }
  zeroify(matrix);
  return matrix;
};

